/**
 * Document Test Data Factory
 *
 * Generates fake document metadata and file buffers for testing.
 */

import { faker } from '@faker-js/faker';

export interface DocumentFactoryData {
  id: string;
  userId: string;
  filename: string;
  storedName: string;
  url: string | null;
  mimeType: string;
  size: number;
  entityType: string;
  entityId: string;
  createdAt: Date;
}

let documentCounter = 0;

/**
 * Create fake document metadata.
 */
export function createDocument(
  userId: string,
  overrides: Partial<DocumentFactoryData> = {}
): DocumentFactoryData {
  documentCounter++;
  const mimeType = overrides.mimeType || faker.helpers.arrayElement([
    'application/pdf',
    'image/jpeg',
    'image/png',
    'application/vnd.openxmlformats-officedocument.spreadsheetml.sheet',
  ]);

  const ext = {
    'application/pdf': '.pdf',
    'image/jpeg': '.jpg',
    'image/png': '.png',
    'application/vnd.openxmlformats-officedocument.spreadsheetml.sheet': '.xlsx',
    'application/vnd.openxmlformats-officedocument.wordprocessingml.document': '.docx',
    'application/msword': '.doc',
    'application/vnd.ms-excel': '.xls',
  }[mimeType] || '.bin';

  const filename = overrides.filename || `test-document-${documentCounter}${ext}`;
  const storedName = overrides.storedName || `${faker.string.uuid()}${ext}`;

  return {
    id: overrides.id || `test-doc-${documentCounter}-${faker.string.alphanumeric(8)}`,
    userId,
    filename,
    storedName,
    url: overrides.url || `https://mock-blob.vercel-storage.com/${storedName}`,
    mimeType,
    size: overrides.size || faker.number.int({ min: 1024, max: 5 * 1024 * 1024 }),
    entityType: overrides.entityType || faker.helpers.arrayElement(['asset', 'liability']),
    entityId: overrides.entityId || faker.string.alphanumeric(25),
    createdAt: overrides.createdAt || new Date(),
  };
}

